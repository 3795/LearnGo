package main

import "fmt"

/**
删除排序数组中的重复项
https://leetcode-cn.com/problems/remove-duplicates-from-sorted-array/
*/

func main() {
	nums := []int{0, 0, 1, 1, 1, 2, 2, 3, 3, 4}
	fmt.Println(removeDuplicates(nums))
	fmt.Printf("%v", nums)
}

func removeDuplicates(nums []int) int {
	k := 0
	for i := 1; i < len(nums); i++ {
		if nums[i] != nums[k] {
			k++
			nums[k] = nums[i]
		}
	}
	return k + 1
}
